import { execFileSync } from "node:child_process";
import { existsSync, readFileSync, readdirSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";

const repositoryRoot = resolve(new URL("..", import.meta.url).pathname);
const defaultSourcePath = resolve(repositoryRoot, "../languages/linguistic-terminology");
const outputPath = resolve(repositoryRoot, "packages/language/linguistic-terminology-snapshot.ts");

const sourcePath = resolve(readSourcePath(process.argv.slice(2)));

if (!existsSync(sourcePath)) {
  fail(`Linguistic terminology source repository was not found: ${sourcePath}`);
}

const termsDirectory = resolve(sourcePath, "terms");
if (!existsSync(termsDirectory)) {
  fail(`Linguistic terminology terms directory was not found: ${termsDirectory}`);
}

const sourceCommit = readGitCommit(sourcePath);
const terms = readTerms(termsDirectory);
validateTerms(terms);

const snapshot = {
  sourceRepository: "linguistic-terminology",
  sourceCommit,
  terms
};

const content = `// Generated by scripts/sync-linguistic-terminology.mjs. Do not edit by hand.

import type { LinguisticTerminologySnapshot } from "./linguistic-terminology";

export const linguisticTerminologySnapshot: LinguisticTerminologySnapshot = ${JSON.stringify(snapshot, null, 2)};
`;

const existing = existsSync(outputPath) ? readFileSync(outputPath, "utf8") : "";
if (existing !== content) {
  writeFileSync(outputPath, content, "utf8");
  console.log(`Synchronized ${terms.length} linguistic terminology terms.`);
} else {
  console.log(`Linguistic terminology snapshot is already current (${terms.length} terms).`);
}

function readSourcePath(args) {
  const sourceFlagIndex = args.indexOf("--source");
  if (sourceFlagIndex >= 0) {
    const value = args[sourceFlagIndex + 1];
    if (value === undefined || value.trim().length === 0) {
      fail("Usage: npm run sync-linguistic-terminology -- --source <path>");
    }
    return value;
  }

  return process.env.LINGUISTIC_TERMINOLOGY_SOURCE ?? defaultSourcePath;
}

function readGitCommit(path) {
  try {
    return execFileSync("git", ["-C", path, "rev-parse", "HEAD"], { encoding: "utf8" }).trim();
  } catch (error) {
    if (typeof error === "object" && error !== null && "stdout" in error && typeof error.stdout === "string") {
      const stdout = error.stdout.trim();
      if (/^[0-9a-f]{40}$/u.test(stdout)) {
        return stdout;
      }
    }

    return null;
  }
}

function readTerms(directory) {
  const markdownFiles = readdirSync(directory)
    .filter((file) => file.endsWith(".md"))
    .sort((left, right) => left.localeCompare(right));

  const terms = [];
  for (const file of markdownFiles) {
    const text = readFileSync(resolve(directory, file), "utf8");
    const blocks = text.split(/\n(?=## )/u).filter((block) => block.startsWith("## "));

    for (const block of blocks) {
      const heading = requiredMatch(block, /^## (.+)$/mu, `heading in ${file}`);
      const id = requiredMatch(block, /\*\*ID:\*\* `([^`]+)`/u, `ID for ${heading}`);
      const category = requiredMatch(block, /\*\*Category:\*\*\s*([^\n]+)/u, `category for ${heading}`);
      const alsoCalled = optionalInlineField(block, "Also called");
      const shortDefinition = requiredBlockField(block, "Short definition", heading);
      const explanation = requiredBlockField(block, "Explanation", heading);
      const example = optionalBlockField(block, "Example");
      const relatedTermsText = optionalBlockField(block, "Related terms");

      terms.push({
        id,
        heading,
        aliases: splitAliases(alsoCalled),
        category,
        sourceFile: `terms/${file}`,
        sourceAnchor: slugifyHeading(heading),
        shortDefinition,
        explanation,
        ...(example === null ? {} : { example }),
        relatedTerms: parseRelatedTerms(relatedTermsText)
      });
    }
  }

  return terms.sort((left, right) => left.heading.localeCompare(right.heading) || left.id.localeCompare(right.id));
}

function validateTerms(terms) {
  const ids = new Map();
  const anchors = new Set(terms.map((term) => `${term.sourceFile}#${term.sourceAnchor}`));

  for (const term of terms) {
    if (!/^[a-z]+(?:[.-][a-z0-9]+)*$/u.test(term.id) || !term.id.includes(".")) {
      fail(`Invalid stable term ID: ${term.id}`);
    }

    if (ids.has(term.id)) {
      fail(`Duplicate stable term ID: ${term.id}`);
    }
    ids.set(term.id, term);

    for (const relatedTerm of term.relatedTerms) {
      const key = resolveRelatedTarget(term.sourceFile, relatedTerm.target);
      if (key !== null && !anchors.has(key)) {
        fail(`Broken related-term link in ${term.id}: ${relatedTerm.target}`);
      }
    }
  }
}

function requiredMatch(text, pattern, label) {
  const match = text.match(pattern);
  if (match === null || match[1].trim().length === 0) {
    fail(`Missing ${label}`);
  }
  return match[1].trim();
}

function requiredBlockField(text, label, heading) {
  const value = optionalBlockField(text, label);
  if (value === null || value.length === 0) {
    fail(`Missing ${label} for ${heading}`);
  }
  return value;
}

function optionalInlineField(text, label) {
  const match = text.match(new RegExp(`\\*\\*${escapeRegExp(label)}:\\*\\*\\s*([^\\n]+)`, "u"));
  return match === null ? null : match[1].trim().replace(/\s{2,}$/u, "");
}

function optionalBlockField(text, label) {
  const match = text.match(new RegExp(`\\*\\*${escapeRegExp(label)}:\\*\\*\\s*(?:\\n|  \\n)([\\s\\S]*?)(?=\\n\\n\\*\\*[^*]+:\\*\\*|$)`, "u"));
  return match === null ? null : normalizeMarkdownValue(match[1]);
}

function normalizeMarkdownValue(value) {
  return value
    .trim()
    .split("\n")
    .map((line) => line.trimEnd())
    .join("\n");
}

function splitAliases(value) {
  if (value === null || value.length === 0) {
    return [];
  }

  return value
    .split(/,\s*/u)
    .map((alias) => alias.trim())
    .filter((alias) => alias.length > 0);
}

function parseRelatedTerms(value) {
  if (value === null || value.length === 0) {
    return [];
  }

  return [...value.matchAll(/\[([^\]]+)\]\(([^)]+)\)/gu)].map((match) => ({
    label: match[1],
    target: match[2]
  }));
}

function resolveRelatedTarget(sourceFile, target) {
  const [targetFile, anchor] = target.split("#");
  if (anchor === undefined || anchor.length === 0) {
    return null;
  }

  if (targetFile.length === 0) {
    return `${sourceFile}#${anchor}`;
  }

  if (targetFile.includes("/") || targetFile.startsWith("..")) {
    return null;
  }

  return `terms/${targetFile}#${anchor}`;
}

function slugifyHeading(heading) {
  return heading
    .trim()
    .toLowerCase()
    .replace(/[^\p{Letter}\p{Number}\s-]/gu, "")
    .replace(/\s+/gu, "-");
}

function escapeRegExp(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function fail(message) {
  console.error(message);
  process.exit(1);
}
